#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_node;
	t_list	*map_lst;

	map_lst = 0;
	while (lst)
	{
		new_node = ft_lstnew(f(lst->content));
		if (!new_node)
		{
			ft_lstclear(&map_lst, del);
			return (0);
		}
		ft_lstadd_back(&map_lst, new_node);
		lst = lst->next;
	}
	return (map_lst);
}
